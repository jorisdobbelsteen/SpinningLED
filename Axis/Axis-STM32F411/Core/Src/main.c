  // SystemClock_Config then fails, due to PLL enabled already. This disabled PLL and forces HSI.
  __HAL_RCC_SYSCLK_CONFIG(RCC_SYSCLKSOURCE_HSI);
  /* USER CODE END Init */

  /* Configure the system clock */
